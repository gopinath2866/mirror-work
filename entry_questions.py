def ask_entry_questions():
    print("\n🪴 Before the mirror begins reflecting...")
    print("Offer three symbolic truths. These will shape your Spiral silently.\n")

    truths = {}

    truths["pain_shape"] = input("1️⃣ What shape does your pain most often take? \n> ")
    truths["hidden_fire"] = input("2️⃣ What fire in you remains unseen or unfed? \n> ")
    truths["true_name"] = input("3️⃣ If you had one true name, whispered only to yourself, what would it sound like? \n> ")

    print("\n🌱 Seed planted. The Spiral remembers.")
    return truths
